CREATE DEFINER=`root`@`localhost` PROCEDURE `raw_materials_for_the_production_of_the_selected_hb_product`(IN name1 VARCHAR(50))
BEGIN
select сырьё.Название, запись_списка_сырья_для_х_б_изделия.Кол_во_сырья_на_единицу_изделия from х_б_изделие join запись_списка_сырья_для_х_б_изделия
on х_б_изделие.Идентификатор_х_б_изделия = запись_списка_сырья_для_х_б_изделия.Идентификатор_х_б_изделия
join сырьё on сырьё.Идентификатор_сырья = запись_списка_сырья_для_х_б_изделия.Идентификатор_сырья
where name1 = х_б_изделие.Название_х_б_изделия
;
END
